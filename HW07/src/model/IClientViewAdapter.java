package model;

/**
 * Model to View Adapter of Client
 * Used by model to communicate with view
 * @author Zihan Wang, Yiqing Lu
 *
 */
public interface IClientViewAdapter {
	
	/**
	 * Add output status to the JTextArea to be shown
	 * @param string the string to be output
	 */
	public void outputToTextArea(String string);
	
	/**
	 * Use the current computer's address to be shown
	 * on the view as the initial remote host address
	 * @param address
	 */
	public void setRemoteAdress (String address);
	
}
