package model.strategy.paint.shape;

/**
 * The factory that stores a shape.
 * @author HaoyuanYue
 * @author Yiqing Lu
 */

import java.awt.Shape;

public interface IShapeFactory {

	/**
	 * get a shape centered in (x,y) with the specific dimensions.
	 * @param x floating point x-coordinate of center of shape
	 * @param y floating point y-coordinate of center of shape
	 * @param xScale floating point x-radius of the shape
	 * @param yScale floating point y-radius of the shape
	 * @return a shape instance
	 */

	public Shape makeShape(double x, double y, double xScale, double yScale);
}
